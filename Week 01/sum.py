#Write a program that takes two numbers and prints their sum.

num1 = int(input("Enter the first number: ")) #input() function is used to take input from the user
num2 = int(input("Enter the second number: ")) #int() function is used to convert the input into integer

sum = num1 + num2 #sum of the two numbers

print("The sum of the two numbers is: ", sum) #print() function is used to display the output