#get the number as input and do addition , substraction, multiplication of those two numbers

#inputs
first_num = int(input('enter first number :'))
second_num = int(input('enter second number :'))

#operations

#+=
first_num += second_num
print(f"Adds and assigns value to first num:{first_num}")

#-=
first_num -= second_num
print(f"Substracts and assigns value first num:{first_num}")

#*=
first_num *= second_num
print(f"Multiplies and assigns value first num:{first_num}")

#/=
first_num /= second_num
print(f"divide and assigns value first num:{first_num}")

#%=
first_num %= second_num
print(f"mods and assigns value first num:{first_num}")

#**=
first_num **= second_num
print(f"Exponent and assigns value first num:{first_num}")

#//=
first_num //= second_num
print(f"Floor and assigns value first num:{first_num}")



